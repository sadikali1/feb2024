package com.operators;

public class Arithmetic {

	public static void main(String[] args) {
		int a = 25;
		int b = 10;
		
		int sum = a+b;
		System.out.println(sum);
		
		int minus = a-b;
		System.out.println(minus);
		
		int mul = a*b;
		System.out.println(mul);
		
		int div = a/b;
		System.out.println(div);
		
		int remainder = a%b;
		System.out.println(remainder);
	}

}

